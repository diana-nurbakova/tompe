"""Dashboard visualizations for the MasteryGap (Wasserstein) metric.

Implements the three teacher-facing visualization prototypes from
``specs/dashboard_visualizations_spec.md``:

- Option A: Student skill profile with optimal-transport arrows
- Option B: Trajectory sparklines with an alert band
- Option C: Class heatmap with session progression

Each function consumes the same Track B synthetic data already generated by
:mod:`experiments.wasserstein.synthetic_trajectories` and the M2 graph ground
metric from :mod:`experiments.wasserstein.ground_metrics`. The Wasserstein
machinery is intentionally hidden — the metric is presented only as
``MasteryGap`` so the figures remain interpretable to a teacher.

Run as a module to regenerate every figure into
``outputs/wasserstein/figures/dashboard/``::

    python -m experiments.wasserstein.dashboard_visualizations
"""

from __future__ import annotations

import logging
from pathlib import Path

import matplotlib.pyplot as plt
import numpy as np
import ot
import seaborn as sns
from matplotlib.patches import FancyArrowPatch

from .config import N_SESSIONS, N_SKILLS, SKILL_LABELS, SKILL_NAMES, TARGET_PROFILE
from .ground_metrics import build_unweighted_graph
from .synthetic_trajectories import StudentTrajectory, generate_all_students

logger = logging.getLogger(__name__)


# Reuse the archetype palette from the main visualizations module so the
# dashboard prototypes stay visually consistent with paper figures F1–F12.
ARCHETYPE_COLORS = {
    "coherent": "#2196F3",
    "scattered": "#FF9800",
    "fast_plateau": "#4CAF50",
    "slow_steady": "#9C27B0",
    "surface_only": "#F44336",
}

ARCHETYPE_SHORT = {
    "coherent": "Coherent",
    "scattered": "Scattered",
    "fast_plateau": "Fast/Plateau",
    "slow_steady": "Slow Steady",
    "surface_only": "Surface-Only",
}

# Display labels combining the skill code and the human-readable name.
SKILL_DISPLAY = [f"{k} {SKILL_NAMES[k]}" for k in SKILL_LABELS]

_EPS = 1e-8


def _setup_style() -> None:
    sns.set_theme(style="whitegrid", font_scale=1.05)
    plt.rcParams["figure.dpi"] = 150
    plt.rcParams["savefig.bbox"] = "tight"


def _profile_to_array(profile: dict[str, float]) -> np.ndarray:
    return np.array([profile[k] for k in SKILL_LABELS], dtype=float)


def _normalise(a: np.ndarray) -> np.ndarray:
    a = np.maximum(a, _EPS)
    return a / a.sum()


def _target_array() -> np.ndarray:
    return np.array([TARGET_PROFILE[k] for k in SKILL_LABELS], dtype=float)


# =============================================================================
# Option A — Student skill profile with transport arrows
# =============================================================================

def _generate_interpretation(
    student: np.ndarray,
    target: np.ndarray,
    plan: np.ndarray,
    skill_names: list[str],
    margin: float = 0.02,
) -> str:
    """Build a one-sentence teacher-facing reading of the transport plan."""
    mu_s = _normalise(student)
    mu_t = _normalise(target)
    excess = [skill_names[i] for i in range(len(skill_names)) if mu_s[i] > mu_t[i] + margin]
    deficit = [skill_names[i] for i in range(len(skill_names)) if mu_t[i] > mu_s[i] + margin]
    if excess and deficit:
        return (
            f"Strong on {', '.join(excess)}. "
            f"Focus next on {', '.join(deficit[:3])}."
        )
    if deficit:
        return f"Needs development on {', '.join(deficit[:3])}."
    return "Profile well-balanced. Continue current approach."


def plot_student_profile_with_transport(
    student_profile: np.ndarray,
    target_profile: np.ndarray,
    ground_metric: np.ndarray,
    skill_names: list[str],
    student_name: str = "Student",
    ax: plt.Axes | None = None,
) -> tuple[plt.Figure, float, np.ndarray]:
    """Horizontal bars for student vs. target with transport arrows overlaid.

    Returns ``(figure, mastery_gap, transport_plan)`` so callers can also
    feed the plan into :func:`_generate_interpretation`.
    """
    student = np.asarray(student_profile, dtype=float)
    target = np.asarray(target_profile, dtype=float)
    n_skills = len(skill_names)

    mu_s = _normalise(student)
    mu_t = _normalise(target)
    plan = ot.emd(mu_s, mu_t, ground_metric)
    gap = float(ot.emd2(mu_s, mu_t, ground_metric))

    if ax is None:
        fig, ax = plt.subplots(figsize=(8.5, 5.0))
    else:
        fig = ax.figure

    y_pos = np.arange(n_skills)[::-1]  # S1 at top
    bar_height = 0.36

    ax.barh(
        y_pos + bar_height / 2,
        target,
        height=bar_height,
        color="#E0E0E0",
        edgecolor="#888888",
        label="Expert target",
    )
    ax.barh(
        y_pos - bar_height / 2,
        student,
        height=bar_height,
        color="#4A90D9",
        edgecolor="#2C5F8A",
        label=student_name,
    )

    # Transport arrows: only off-diagonal flows above a visibility threshold
    arrow_threshold = 0.005
    arrow_x = max(student.max(), target.max()) + 0.05
    for i in range(n_skills):
        for j in range(n_skills):
            if i == j or plan[i, j] <= arrow_threshold:
                continue
            start_y = y_pos[i] - bar_height / 2
            end_y = y_pos[j] + bar_height / 2
            arrow = FancyArrowPatch(
                (arrow_x, start_y),
                (arrow_x, end_y),
                arrowstyle="->",
                mutation_scale=14,
                linewidth=max(0.6, plan[i, j] * 50),
                color="#D35400",
                alpha=0.65,
                connectionstyle="arc3,rad=0.35",
            )
            ax.add_patch(arrow)

    ax.set_yticks(y_pos)
    ax.set_yticklabels(skill_names)
    ax.set_xlim(0, 1.18)
    ax.set_xlabel("Mastery (raw detection rate)")
    ax.set_title(f"{student_name} — MasteryGap: {gap:.3f}")
    ax.legend(loc="lower right", framealpha=0.9)

    interpretation = _generate_interpretation(student, target, plan, skill_names)
    fig.text(
        0.5,
        -0.02,
        interpretation,
        ha="center",
        va="top",
        fontsize=10,
        style="italic",
        color="#333333",
    )

    return fig, gap, plan


def figure_A_student_profiles(
    students: list[StudentTrajectory],
    ground_metric: np.ndarray,
    output_dir: Path,
) -> None:
    """Generate the three Option-A variants from the spec."""
    target = _target_array()
    by_id = {s.student_id: s for s in students}

    # spec §"Variants to produce" for Option A
    variants = [
        ("coherent_0", 4, "DA1_coherent_session5"),  # session 5 (1-indexed)
        ("surface_only_0", 7, "DA2_surface_only_session8"),
        ("scattered_0", 9, "DA3_scattered_session10"),
    ]
    for sid, session_idx, fname in variants:
        student = by_id.get(sid)
        if student is None:
            logger.warning("Student %s not found, skipping %s", sid, fname)
            continue
        profile = _profile_to_array(student.profiles[session_idx])
        display_name = f"{ARCHETYPE_SHORT[student.archetype]} ({sid}) — Session {session_idx + 1}"
        fig, gap, _ = plot_student_profile_with_transport(
            profile,
            target,
            ground_metric,
            SKILL_DISPLAY,
            student_name=display_name,
        )
        out = output_dir / f"{fname}.png"
        fig.savefig(out)
        plt.close(fig)
        logger.info("Saved %s (MasteryGap=%.3f)", out, gap)


# =============================================================================
# Option B — Trajectory sparklines with alert band
# =============================================================================

def _compute_trajectories(
    students: list[StudentTrajectory], ground_metric: np.ndarray
) -> dict[str, dict]:
    """Pre-compute MasteryGap-per-session for every student."""
    target = _normalise(_target_array())
    out: dict[str, dict] = {}
    for s in students:
        gaps = []
        for profile in s.profiles:
            mu_s = _normalise(_profile_to_array(profile))
            gaps.append(float(ot.emd2(mu_s, target, ground_metric)))
        out[s.student_id] = {"gaps": np.array(gaps), "archetype": s.archetype}
    return out


def _alert_band(trajectories: dict[str, dict]) -> tuple[np.ndarray, np.ndarray, np.ndarray]:
    matrix = np.array([t["gaps"] for t in trajectories.values()])
    medians = np.median(matrix, axis=0)
    mads = np.median(np.abs(matrix - medians), axis=0)
    upper = medians + 1.5 * mads
    lower = np.maximum(0.0, medians - 1.5 * mads)
    return medians, lower, upper


def _plot_sparklines(
    trajectories: dict[str, dict],
    selected_ids: list[str] | None,
    title: str,
    figsize: tuple[float, float],
    show_axes: bool = True,
    annotate: bool = False,
) -> plt.Figure:
    medians, lower, upper = _alert_band(trajectories)
    n_sessions = len(medians)
    sessions = np.arange(1, n_sessions + 1)

    fig, ax = plt.subplots(figsize=figsize)
    ax.fill_between(
        sessions, lower, upper, color="#BDBDBD", alpha=0.35, label="Expected range"
    )
    ax.plot(sessions, medians, color="#666666", linewidth=1.0, linestyle="--", label="Class median")

    ids = selected_ids if selected_ids is not None else list(trajectories.keys())
    for sid in ids:
        traj = trajectories[sid]
        colour = ARCHETYPE_COLORS.get(traj["archetype"], "gray")
        # Override to red if final point exits the band on the high side
        if traj["gaps"][-1] > upper[-1]:
            colour = "#D0021B"
        ax.plot(
            sessions,
            traj["gaps"],
            color=colour,
            alpha=0.85,
            linewidth=1.8 if selected_ids else 1.2,
        )
        if annotate:
            ax.text(
                sessions[-1] + 0.1,
                traj["gaps"][-1],
                ARCHETYPE_SHORT.get(traj["archetype"], sid),
                color=colour,
                fontsize=9,
                va="center",
            )

    if show_axes:
        ax.set_xlabel("Session")
        ax.set_ylabel("MasteryGap")
        ax.set_title(title)
        ax.set_xticks(sessions)
        ax.legend(loc="upper right", framealpha=0.9, fontsize=9)
    else:
        ax.set_xticks([])
        ax.set_yticks([])
        for spine in ax.spines.values():
            spine.set_visible(False)
        ax.set_title(title, fontsize=10)

    ax.set_xlim(0.8, n_sessions + (1.8 if annotate else 0.4))
    return fig


def figure_B_trajectories(
    students: list[StudentTrajectory],
    ground_metric: np.ndarray,
    output_dir: Path,
) -> None:
    """Generate the three Option-B variants from the spec."""
    trajectories = _compute_trajectories(students, ground_metric)

    # Variant 1: full class overview, all 20 students coloured by archetype
    fig = _plot_sparklines(
        trajectories,
        selected_ids=None,
        title="Class trajectories (all students)",
        figsize=(8, 4),
    )
    fig.savefig(output_dir / "DB1_class_overview.png")
    plt.close(fig)

    # Variant 2: focused 3-student view (Coherent, Surface-Only, Slow Steady)
    focus_ids = [
        sid
        for sid in ("coherent_0", "surface_only_0", "slow_steady_0")
        if sid in trajectories
    ]
    fig = _plot_sparklines(
        trajectories,
        selected_ids=focus_ids,
        title="Representative trajectories",
        figsize=(7.5, 3.6),
        annotate=True,
    )
    fig.savefig(output_dir / "DB2_focused_three.png")
    plt.close(fig)

    # Variant 3: compact sparkline strip for table-row embedding
    fig = _plot_sparklines(
        trajectories,
        selected_ids=focus_ids,
        title="",
        figsize=(4.0, 1.2),
        show_axes=False,
    )
    fig.savefig(output_dir / "DB3_compact_strip.png")
    plt.close(fig)
    logger.info("Saved Option B trajectory sparklines")


# =============================================================================
# Option C — Class heatmap with session progression
# =============================================================================

def _build_gap_matrix(
    students: list[StudentTrajectory], ground_metric: np.ndarray
) -> tuple[np.ndarray, list[str], list[str]]:
    target = _normalise(_target_array())
    rows, ids, archetypes = [], [], []
    for s in students:
        gaps = []
        for profile in s.profiles:
            mu_s = _normalise(_profile_to_array(profile))
            gaps.append(float(ot.emd2(mu_s, target, ground_metric)))
        rows.append(gaps)
        ids.append(s.student_id)
        archetypes.append(s.archetype)
    return np.array(rows), ids, archetypes


def _trend_arrow(gaps: np.ndarray, threshold: float = 0.02) -> str:
    delta = gaps[-1] - gaps[0]
    if delta < -threshold:
        return "↓"
    if delta > threshold:
        return "↑"
    return "→"


def _heatmap(
    matrix: np.ndarray,
    labels: list[str],
    n_sessions: int,
    title: str,
    figsize: tuple[float, float],
    label_colors: list[str] | None = None,
    row_dividers: list[int] | None = None,
) -> plt.Figure:
    fig, ax = plt.subplots(figsize=figsize)
    sns.heatmap(
        matrix,
        ax=ax,
        xticklabels=range(1, n_sessions + 1),
        yticklabels=labels,
        cmap="RdYlGn_r",
        vmin=float(matrix.min()),
        vmax=float(matrix.max()),
        cbar_kws={"label": r"MasteryGap ($W_1$ to target)"},
        linewidths=0.3,
        linecolor="white",
    )
    if label_colors is not None:
        for tick, colour in zip(ax.get_yticklabels(), label_colors):
            tick.set_color(colour)
    if row_dividers:
        for r in row_dividers:
            ax.axhline(r, color="black", linewidth=1.2)
    ax.set_xlabel("Session")
    ax.set_ylabel("Student")
    ax.set_title(title)
    return fig


def figure_C_class_heatmap(
    students: list[StudentTrajectory],
    ground_metric: np.ndarray,
    output_dir: Path,
) -> None:
    """Generate the three Option-C variants from the spec."""
    matrix, ids, archetypes = _build_gap_matrix(students, ground_metric)
    n_sessions = matrix.shape[1]

    # Variant 1: sorted by final MasteryGap (best at top)
    final_gaps = matrix[:, -1]
    order = np.argsort(final_gaps)
    sorted_matrix = matrix[order]
    sorted_labels = [
        f"{_trend_arrow(matrix[i])} {ids[i]}" for i in order
    ]
    fig = _heatmap(
        sorted_matrix,
        sorted_labels,
        n_sessions,
        "Class progress overview (sorted by final MasteryGap)",
        figsize=(9, 6),
    )
    fig.savefig(output_dir / "DC1_sorted_by_gap.png")
    plt.close(fig)

    # Variant 2: grouped by archetype, with horizontal dividers
    archetype_order = ["coherent", "slow_steady", "fast_plateau", "scattered", "surface_only"]
    grouped_idx: list[int] = []
    dividers: list[int] = []
    for arch in archetype_order:
        arch_idx = [i for i, a in enumerate(archetypes) if a == arch]
        grouped_idx.extend(arch_idx)
        if grouped_idx and arch != archetype_order[-1]:
            dividers.append(len(grouped_idx))
    grouped_matrix = matrix[grouped_idx]
    grouped_labels = [
        f"{_trend_arrow(matrix[i])} {ids[i]}" for i in grouped_idx
    ]
    fig = _heatmap(
        grouped_matrix,
        grouped_labels,
        n_sessions,
        "Class progress overview (grouped by archetype)",
        figsize=(9, 6),
        row_dividers=dividers,
    )
    fig.savefig(output_dir / "DC2_grouped_by_archetype.png")
    plt.close(fig)

    # Variant 3: compact paper version with archetype-coloured row labels
    label_colors = [ARCHETYPE_COLORS[archetypes[i]] for i in order]
    fig = _heatmap(
        sorted_matrix,
        sorted_labels,
        n_sessions,
        "Class progress overview",
        figsize=(6.5, 5),
        label_colors=label_colors,
    )
    fig.savefig(output_dir / "DC3_compact_paper.png")
    plt.close(fig)
    logger.info("Saved Option C class heatmaps")


# =============================================================================
# Entry point
# =============================================================================

def generate_all_dashboard_figures(
    students: list[StudentTrajectory] | None = None,
    ground_metric: np.ndarray | None = None,
    output_dir: Path | str = Path("outputs/wasserstein/figures/dashboard"),
) -> Path:
    """Regenerate every dashboard figure into ``output_dir``."""
    _setup_style()
    if students is None:
        students = generate_all_students()
    if ground_metric is None:
        ground_metric = build_unweighted_graph()  # M2 graph (recommended in spec)

    out = Path(output_dir)
    out.mkdir(parents=True, exist_ok=True)
    logger.info("Writing dashboard figures to %s", out)

    figure_A_student_profiles(students, ground_metric, out)
    figure_B_trajectories(students, ground_metric, out)
    figure_C_class_heatmap(students, ground_metric, out)

    return out


def main() -> None:
    logging.basicConfig(
        level=logging.INFO,
        format="%(asctime)s %(name)s %(levelname)s %(message)s",
    )
    generate_all_dashboard_figures()


if __name__ == "__main__":
    main()
